// title of program
// Program to find sum of first 10 natural numbers using for loop
// header file
#include<stdio.h>
// function heading
void main()
{
// variable declaration
int i,sum;
// condition of for loop
for(i=1;i<=10;i++)
{
// input statement for cube
sum=sum+i;
// output statement
printf("sum=sum+i\n",sum);
printf("%d\n",i);
}
}
